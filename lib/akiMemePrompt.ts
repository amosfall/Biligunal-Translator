/**
 * AKI 动态梗：先判定是否「专名向」再决定是否生成毒舌梗（不接外网搜索）。
 * 密文仍对用户原文 encodeAki；此处扩写仅给模型判 eligible / 写梗用。
 */

/** 给模型的提示用扩写（密文仍编用户原文）；减少「简称/地名」被误判为非专名 */
const MEME_PROMPT_EXPAND: Record<string, string> = {
  北大: "北京大学（常用简称「北大」）",
  清华: "清华大学（常用简称「清华」）",
  港大: "香港大学（常用简称「港大」）",
  复旦: "复旦大学（常用简称「复旦」）",
  北京: "北京（中国城市／首都，地名专名）",
  北京大学: "北京大学（中国大陆高等院校）",
};

export function expandChineseUniNicknameForMemePrompt(raw: string): string {
  const t = raw.replace(/\r\n/g, "\n").trim().replace(/\s+/g, " ");
  return MEME_PROMPT_EXPAND[t] ?? t;
}

export function buildAkiMemePrompt(userPhrase: string): string {
  const phraseForModel = expandChineseUniNicknameForMemePrompt(userPhrase);
  return `你是熟悉中文互联网段子与亚文化的文案作者。用户会输入一段话或短语（常为一段原文里的一小段）。下面「用户短语」可能已把常见**高校简称**扩成带校名的说明，请据此将 eligible 判为 true。

第一步：判断 **eligible**（布尔值）——仅当输入**主要是在指代或讨论**下列类型时，eligible 才为 true：
- 高等院校 / 大学 / 学院等（含**单独出现的常用中文简称**，如北大、清华、复旦、港大等，均视为高校专名）；
- 城市、地区、国家地名（含常见别称；**单独「北京」指北京市／首都地名专名**，eligible 须为 true，勿当成普通形容词）；
- 歌手、乐队、音乐人、艺人等文化专名（可带简短修饰，如「东京大学」「万青」）。
- **文学相关**：作家、诗人、名著/篇名、文学流派、文学奖、文学史梗、网络文学 IP 等**可识别专名或固定搭配**（如鲁迅、卡夫卡、《百年孤独》、诺奖梗；泛说「我喜欢看书」而无具体对象则 eligible 为 false）。
- **电影相关**：导演、演员、片名、系列片、类型片、影史名场面/名台词梗、电影节等**可识别专名**（泛说「看电影」而无具体指向则 false）。
- **摄影相关**：摄影师、器材/品牌梗、摄影风格流派、拍照文化 meme 等**可识别专名或圈内说法**（仅「拍照」无专名则 false）。
- **二次元 / ACG 相关**：动画、漫画、游戏、轻小说、虚拟偶像、声优、经典梗/名台词、宅文化专有说法等**可识别专名**（泛说「看动漫」而无具体作品/角色则 false）。

eligible 必须为 **false** 的情况举例：日常寒暄、问候、随口闲聊、抽象概念、普通叙述句、无明确上述专名或文化指向的句子（如「你好呀」「今天天气不错」「帮我翻译」「随便看看书」等）。

第二步：仅当 eligible 为 true 时，再写毒舌梗：
- 风格：**再 mean 一点**——地狱笑话感、损友互怼、阴阳怪气拉满；多用排比、递进与反差（如「来了就想走但走不了」「随机播放」「怀疑人生」这类**气质**，勿照抄例句）；专名相关的**公开刻板印象与网络段子**里随便发挥，怎么缺德怎么好笑怎么来。**中文须写够篇幅**：要有起承转合或至少两三重意象堆叠，避免只写一两句就收尾。
- **开头偏好**：在合适时让中文梗以 **「你是一个X人，」** 起笔（X 可为地域/校籍身份、**读者/影迷/摄影师/宅**等，如「北漂」「海淀做题家」；高校可用「在XX读书的」变体）；文学/影史可用「读过XX的」「看过XX的」；乐队/歌手梗可用「你听XX的，」等**身份开场**。若实在不合适再换别的开头，但整体仍要够毒。
- en：同样刻薄，可平行用 "You're a ___ who..." / "If you've ever..." 这类**身份开场**，与 zh 同一 roast 氛围（不是逐字翻译）；英文用两句左右，信息量与中文大致匹配。
- 硬性边界：不要编造可核验的假新闻；不要涉政；不要针对可识别真人的仇恨、歧视与骚扰。
- 若 eligible 为 false：**zh 与 en 必须为空字符串**，不要写梗。

输出 **唯一** JSON 对象，字段（顺序不限）：
- eligible：布尔值。
- zh：eligible 为 true 时：**约 90～110 个汉字**（约一百字，可略浮动到 85～120 字）的简体中文梗，须为一段连贯吐槽，勿过短；否则 "" 。
- en：eligible 为 true 时：两句左右英文，够 snarky，篇幅与中文梗相称；否则 "" 。

用户短语：${JSON.stringify(phraseForModel)}`;
}
