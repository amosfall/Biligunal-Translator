/**
 * AKI 动态梗：先判定是否「专名向」再决定是否生成毒舌梗（不接外网搜索）。
 */

export function buildAkiMemePrompt(userPhrase: string): string {
  return `你是熟悉中文互联网段子与亚文化的文案作者。用户会输入一段话或短语（常为一段原文里的一小段）。

第一步：判断 **eligible**（布尔值）——仅当输入**主要是在指代或讨论**下列类型时，eligible 才为 true：
- 高等院校 / 大学 / 学院等；
- 城市、地区、国家地名（含常见别称）；
- 歌手、乐队、音乐人、艺人等文化专名（可带简短修饰，如「东京大学」「万青」）。

eligible 必须为 **false** 的情况举例：日常寒暄、问候、随口闲聊、抽象概念、普通叙述句、无明确上述专名指向的句子（如「你好呀」「今天天气不错」「帮我翻译」等）。

第二步：仅当 eligible 为 true 时，再写毒舌梗：
- 风格：**再 mean 一点**——地狱笑话感、损友互怼、阴阳怪气拉满；多用排比与反差（如「来了就想走但走不了」「随机播放」「怀疑人生」这类**气质**，勿照抄例句）；专名相关的**公开刻板印象与网络段子**里随便发挥，怎么缺德怎么好笑怎么来。
- **开头偏好**：在合适时让中文梗以 **「你是一个X人，」** 起笔（X 可为地域/校籍身份等，如「北漂」「海淀做题家」；高校可用「在XX读书的」变体）；若用乐队/歌手梗，可改成「你听XX的，」等同类**身份开场**。若实在不合适再换别的开头，但整体仍要够毒。
- en：同样刻薄，可平行用 "You're a ___ who..." / "If you've ever..." 这类**身份开场**，与 zh 同一 roast 氛围（不是逐字翻译）。
- 硬性边界：不要编造可核验的假新闻；不要涉政；不要针对可识别真人的仇恨、歧视与骚扰。
- 若 eligible 为 false：**zh 与 en 必须为空字符串**，不要写梗。

输出 **唯一** JSON 对象，字段（顺序不限）：
- eligible：布尔值。
- zh：eligible 为 true 时：约 50～80 个汉字的简体中文梗（可略长以容纳开场+排比）；否则 "" 。
- en：eligible 为 true 时：一句或两句英文，够 snarky；否则 "" 。

用户短语：${JSON.stringify(userPhrase)}`;
}
