/**
 * Reads data/uni-nicknames-raw.txt (lines: 学校名 = 梗)
 * Writes lib/uniUserNicknameSupplement.generated.ts
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const input = path.join(root, "data/uni-nicknames-raw.txt");
const out = path.join(root, "lib/uniUserNicknameSupplement.generated.ts");

const text = fs.readFileSync(input, "utf8");
const lines = text.split(/\r?\n/).map((l) => l.trim()).filter(Boolean);

const rows = new Map();
for (const line of lines) {
  const eq = line.indexOf("=");
  if (eq < 1) continue;
  const key = line.slice(0, eq).trim();
  const val = line.slice(eq + 1).trim();
  if (!key || !val) continue;
  if (!rows.has(key)) rows.set(key, []);
  const arr = rows.get(key);
  if (!arr.includes(val)) arr.push(val);
}

const keys = [...rows.keys()].sort((a, b) => a.localeCompare(b, "zh-Hans"));
const obj = {};
for (const k of keys) obj[k] = rows.get(k);

const json = JSON.stringify(obj, null, 2);
const ts = `/* eslint-disable max-len -- generated by scripts/generate-uni-nickname-supplement.mjs */
export const USER_NICKNAME_SUPPLEMENT: Record<string, string[]> = ${json} as Record<string, string[]>;
`;

fs.writeFileSync(out, ts, "utf8");
console.log("Wrote", out, "keys:", keys.length);
